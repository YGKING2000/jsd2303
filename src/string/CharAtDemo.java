public class CharAtDemo {
    public static void main(String[] args) {
        String str = "thinking in Java";
        System.out.println(str.charAt(0));// 't'
        System.out.println(str.charAt(8));// ' '
        System.out.println(str.charAt(12));// 'J'
    }
}
